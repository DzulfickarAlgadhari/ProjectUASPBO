    private void getAllData() {
        try {
            model.setRowCount(0);
            sql.bukaKoneksi();
            ArrayList<Dosen> listDosen = sql.read();
            System.out.println(listDosen.size());
            for (Dosen ds : listDosen) {
                model.addRow(new Object[]{
                ds.getId(),
                ds.getNip(),
                ds.getNama(),
                ds.getGender(),
                ds.getTanggallahir(),
                ds.getAlamat(),
                ds.getNotelp(),
                ds.getEmail(),
                ds.getFakultas(),
                ds.getHomebase()
            });
        } sql.tutupKoneksi();
    } catch (SQLException | ClassNotFoundException ex) {
        JOptionPane.showMessageDialog(null, "Gagal Mengambil Data");
    }
}     
    
