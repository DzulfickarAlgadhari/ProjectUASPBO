    private void btSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSimpanActionPerformed
        // TODO add your handling code here:
        Dosen ds;
        if (status.equals("add")) {
            try {
                if (txtNama.getText().isEmpty() || txtGender.getText().isEmpty()
                        || txtFakultas.getText().isEmpty() || txtAlamat.getText().isEmpty() || txtNip.getText().isEmpty() 
                        || txtNotelp.getText().isEmpty() || txtHomebase.getText().isEmpty() || txtEmail.getText().isEmpty()
                        || txtTanggal.getText().isEmpty()) {
                    JOptionPane.showMessageDialog(null, "Data harus diisi semua");
                } else {
                    sql.bukaKoneksi();
                    ds = new Dosen();
                    ds.setNama(txtNama.getText());
                    ds.setNip(txtNip.getText());
                    ds.setGender(txtGender.getText());
                    ds.setHomebase(txtHomebase.getText());
                    ds.setFakultas(txtFakultas.getText());
                    ds.setAlamat(txtAlamat.getText());
                    ds.setNotelp(txtNotelp.getText());
                    ds.setEmail(txtEmail.getText());
                    ds.setTanggallahir(txtTanggal.getText());
                    sql.create(ds);
                    getAllData();
                    readData();
                    sql.tutupKoneksi();
                    JOptionPane.showMessageDialog(null, "Data berhasil disimpan");
                }
            } catch (SQLException | ClassNotFoundException ex) {
                JOptionPane.showMessageDialog(null, "Data gagal disimpan");
            }
        } else {
            try {
                if (txtNama.getText().isEmpty() || txtGender.getText().isEmpty()
                        || txtFakultas.getText().isEmpty() || txtAlamat.getText().isEmpty() || txtNip.getText().isEmpty() 
                        || txtNotelp.getText().isEmpty() || txtHomebase.getText().isEmpty() || txtEmail.getText().isEmpty()
                        || txtTanggal.getText().isEmpty()) {
                    JOptionPane.showMessageDialog(null, "Data harus diisi semua");
                } else {
                    sql.bukaKoneksi();
                    ds = new Dosen();
                    ds.setId(Integer.parseInt(txtId.getText()));
                     ds.setNama(txtNama.getText());
                    ds.setNip(txtNip.getText());
                    ds.setGender(txtGender.getText());
                    ds.setHomebase(txtHomebase.getText());
                    ds.setFakultas(txtFakultas.getText());
                    ds.setAlamat(txtAlamat.getText());
                    ds.setNotelp(txtNotelp.getText());
                    ds.setEmail(txtEmail.getText());
                    ds.setTanggallahir(txtTanggal.getText());
                    sql.update(ds);
                    getAllData();
                    readData();
                    sql.tutupKoneksi();
                    JOptionPane.showMessageDialog(null, "Data berhasil disimpan");
                }
            } catch (SQLException | ClassNotFoundException ex) {
                JOptionPane.showMessageDialog(null, "Data gagal disimpan");
            }
        }
    }//GEN-LAST:event_btSimpanActionPerformed

    private void btHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btHapusActionPerformed
        // TODO add your handling code here:
        if (ds == null) {
            JOptionPane.showMessageDialog(null, "Pilih data dahulu");
        } else {
            try {
                int dialogButton = JOptionPane.YES_NO_OPTION;
                int dialogResult = JOptionPane.showConfirmDialog(null, "Hapus data?", "Warning", dialogButton);
                if (dialogResult == JOptionPane.YES_OPTION) {
                    sql.bukaKoneksi();
                    sql.delete(ds);
                    getAllData();
                    sql.tutupKoneksi();
                    ds = null;
                    kosongkanTeks();
                }
            } catch (SQLException | ClassNotFoundException ex) {
                JOptionPane.showMessageDialog(null, "Hapus data gagal");
            }
        }
    }//GEN-LAST:event_btHapusActionPerformed

